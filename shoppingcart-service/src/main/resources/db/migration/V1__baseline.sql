CREATE TABLE PRODUCT_CATEGORY
(
  PRODUCT_CATEGORY_NAME VARCHAR(255) NOT NULL PRIMARY KEY,
  DESCRIPTION           VARCHAR(255),
  CREATED_DATE          TIMESTAMP
);

CREATE TABLE PRODUCT
(
  SKU_NUMBER            VARCHAR(25) NOT NULL PRIMARY KEY,
  PRODUCT_NAME          VARCHAR(255),
  DESCRIPTION           VARCHAR(255),
  INVENTORY_COUNT       INTEGER,
  PRICE                 DOUBLE PRECISION,
  CREATED_DATE          TIMESTAMP,

  PRODUCT_CATEGORY_NAME VARCHAR(255)
    CONSTRAINT FK_PRODUCT_PRODUCT_CATEGORY
    REFERENCES PRODUCT_CATEGORY
);

CREATE TABLE CART
(
  CART_NAME    VARCHAR(255) NOT NULL PRIMARY KEY,
  DESCRIPTION  VARCHAR(255),
  STATUS       VARCHAR(25),
  CREATED_DATE TIMESTAMP
);

CREATE TABLE ITEM
(
  ITEM_ID      BIGINT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  QUANTITY     INTEGER,
  CREATED_DATE TIMESTAMP,

  SKU_NUMBER   VARCHAR(25)
    CONSTRAINT FK_ITEM_PRODUCT
    REFERENCES PRODUCT,

  CART_NAME      VARCHAR(255)
    CONSTRAINT FK_ITEM_CART
    REFERENCES CART
);